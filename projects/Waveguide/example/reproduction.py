def annulus_invariant() -> dict[str, object]:
    field = circular_metal_field("TM", 1, 1, 0.03, 120, 0.5)
    return {
        "shape": field["field"].shape,
        "fc_ghz": field["fc_ghz"],
        "has_inner_mask": bool(field["boundary_radii"][0] == 0.5 and field["field"][60, 60] != field["field"][60, 60]),
    }


def main(example: str | None = None, output: str | Path | None = None) -> list[Path]:
    runners = {
        "rectangle_TE": rectangle_te,
        "planar_TE": planar_te,
        "annulus_TM": annulus_tm,
    }
    if example is None:
        parser = argparse.ArgumentParser()
        parser.add_argument("example", choices=sorted(runners))
        parser.add_argument("--output", type=Path)
        args = parser.parse_args()
        example = args.example
        output = args.output
    return runners[example](output)


if __name__ == "__main__":
    main()

