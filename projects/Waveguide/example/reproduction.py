from __future__ import annotations

import argparse
from pathlib import Path
from typing import Iterable

from matplotlib import pyplot as plt

from projects.Waveguide.core import model
from projects.Waveguide.core.metal import circular_metal_field
from utils import image_output
from utils.image_output import write_manifest


PROJECT = "Waveguide"


def _format_value(value: object) -> str:
    if isinstance(value, (list, tuple)):
        if value and isinstance(value[0], (list, tuple)):
            rows = [" ".join(str(item) for item in row) for row in value]
            return "[" + ";".join(rows) + "]"
        return "[" + " ".join(str(item) for item in value) + "]"
    return str(value)


def _write_parameters(folder: Path, params: dict[str, object]) -> Path:
    lines = ["Generated by Python reproduction", ""]
    lines.extend(f"{key} = {_format_value(value)}" for key, value in params.items())
    path = folder / "parameters.txt"
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return path


def _write_reproduce_code(folder: Path, example: str) -> Path:
    path = folder / "reproduce_code.py"
    path.write_text(
        "from projects.Waveguide.example.reproduction import main\n\n"
        f"if __name__ == '__main__':\n    main('{example}')\n",
        encoding="utf-8",
    )
    return path


def _export_named_figures(bundle, folder: Path, names: Iterable[str]) -> list[Path]:
    paths: list[Path] = []
    for fig, name in zip(bundle.figures, names):
        paths.append(image_output.save_figure(fig, folder, name, title_band=None))
    if len(paths) > 1:
        paths.append(image_output.compose_grid(paths, folder / "composite.png"))
    plt.close("all")
    return paths


def rectangle_te(output: str | Path | None = None) -> list[Path]:
    folder = image_output.ensure_dir(output or Path(__file__).parent / "rectangle_TE")
    modes = [(m, n) for m in range(1, 4) for n in range(0, 4)]
    params = {
        "guide": "rectangular",
        "waveguide_kind": "metal",
        "action": "mode field",
        "polarization": "TE",
        "mode_type": "TE",
        "modes": ";".join(f"({m},{n})" for m, n in modes),
        "grid_n": 240,
        "samples": 260,
        "a": 0.03,
        "xi0": 0.5,
        "b": 0.015,
        "mode_matrix": modes,
    }
    bundle = model.render(params)
    names = [f"{index:02d}_te_rectangular_m{m}_n{n}.png" for index, (m, n) in enumerate(modes, start=1)]
    outputs = _export_named_figures(bundle, folder, names)
    outputs.extend([_write_parameters(folder, params), _write_reproduce_code(folder, "rectangle_TE")])
    outputs.append(write_manifest(folder, PROJECT, "rectangle_TE", [params], outputs, bundle.report))
    return outputs


