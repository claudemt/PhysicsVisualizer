function project_graphics_style()
%PROJECT_GRAPHICS_STYLE Apply project-wide graphics defaults.
set(groot, 'defaultAxesFontName', 'Times New Roman');
set(groot, 'defaultTextFontName', 'Times New Roman');
set(groot, 'defaultLegendFontName', 'Times New Roman');
set(groot, 'defaultUicontrolFontName', 'Times New Roman');
set(groot, 'defaultColorbarFontName', 'Times New Roman');
set(groot, 'defaultAxesFontSize', 15);
set(groot, 'defaultTextFontSize', 16);
set(groot, 'defaultLegendFontSize', 15);
set(groot, 'defaultColorbarFontSize', 15);
end
